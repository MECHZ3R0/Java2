package secondpackage;

public class Dowhileloops1 {
    public static void main(String[] args) {
        int c = 1;
        do{
            System.out.println(2*c);
            c++;
        }
        while(c<=20);
        c=1;
        do{
            System.out.println(c*c);
            c++;
        }
        while(c<=20);
    }
}
